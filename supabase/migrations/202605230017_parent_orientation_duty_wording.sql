with parent_event as (
  select id
  from public.events
  where slug = 'parent-orientation-staff-2569'
  limit 1
)
update public.event_staff_duty_quotas q
set
  duty_label_th = seed.duty_label_th,
  description_th = seed.description_th,
  updated_at = now()
from parent_event
join (
  values
    ('traffic', 'ฝ่ายจราจร', 'ดูแลการเดินทาง การบอกทาง และช่วยจัดระเบียบเส้นทางภายในพื้นที่กิจกรรม'),
    ('medical', 'ฝ่ายพยาบาล', 'ดูแลการปฐมพยาบาลเบื้องต้น ประสานงานกรณีฉุกเฉิน และช่วยดูแลความปลอดภัยของผู้เข้าร่วม'),
    ('registration', 'ฝ่ายลงทะเบียน', 'ตรวจสอบรายชื่อ ต้อนรับผู้เข้าร่วม และช่วยจัดการจุดลงทะเบียนให้เป็นระเบียบ'),
    ('welfare', 'ฝ่ายสวัสดิการ', 'ดูแลอาหาร น้ำดื่ม อุปกรณ์ และความเรียบร้อยด้านสวัสดิการของทีมงานและผู้เข้าร่วม'),
    ('benefits_sales', 'ฝ่ายสิทธิประโยชน์', 'ดูแลบูธสิทธิประโยชน์ การจำหน่ายสินค้า หรือกิจกรรมสนับสนุนรายได้และประชาสัมพันธ์ของงาน'),
    ('registration_it', 'ฝ่ายสนับสนุนระบบลงทะเบียน (IT)', 'ช่วยดูแลอุปกรณ์ ระบบลงทะเบียน การแก้ปัญหาหน้างาน และการประสานงานด้านเทคนิค'),
    ('backstage', 'ฝ่ายประสานงานเวที', 'ดูแลหลังเวที คิวกิจกรรม การประสานงานผู้เกี่ยวข้อง และความพร้อมของช่วงพิธีการหรือการแสดง'),
    ('general', 'ฝ่ายทั่วไป', 'สนับสนุนงานทั่วไปตามที่ได้รับมอบหมาย เช่น ช่วยประจำจุดต่าง ๆ อำนวยความสะดวก และช่วยเสริมกำลังฝ่ายที่ต้องการคนเพิ่ม')
) as seed(duty_key, duty_label_th, description_th) on true
where q.event_id = parent_event.id
  and q.duty_key = seed.duty_key;
